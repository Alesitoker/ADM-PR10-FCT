package com.iessaladillo.alejandro.adm_pr10_fct.base;

public class TransferSelect {

    private long id;
    private String name;

    public TransferSelect(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
